Firma nasza organizuje szkolenia przygotowujące słuchaczy do egzaminów kwalifikacyjnych we wszystkich trzech grupach i w pełnym zakresie.<br />

Współpracujemy ponadto z Komisjami Kwalifikacyjnymi, co zapewnia przystąpienie do egzaminu bezpośrednio po ukończonym szkoleniu.<br />

Koszt szkolenia zależny jest od ilości szkolonych osób oraz zakresu tego szkolenia i uzgadniany jest każdorazowo z kontrahentem.<br />

