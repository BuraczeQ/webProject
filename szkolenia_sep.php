            <h1 style="color:#612f00;"> Energetyczne uprawnienia kwalifikacyjne</h1>
        <p>
        	Zgodnie z wymogami Ustawy Prawo Energetyczne z dnia 10 kwietnia 1997 r. (z późniejszymi zmianami), osoby zajmujące się eksploatacją sieci oraz urządzeń i instalacji zobowiązane są posiadać kwalifikacje potwierdzone świadectwem wydanym przez komisje kwalifikacyjne.
Obowiązek ten nie dotyczy wyłącznie użytkowników eksploatujących:<br />
- urządzenia elektryczne o napięciu nie wyższym niż 1 kV i mocy znamionowej nie wyższej niż 20 kW<br />
- urządzenia i instalacje cieplne o mocy zainstalowanej nie wyższej niż 50 kW.<br /><br />

Zasady uzyskiwania świadectw kwalifikacyjnych omówione są w Rozp. Min. Gosp., Pracy i Polityki Społ. z dn. 28 kwietnia 2003 r. w sprawie szczegółowych zasad stwierdzania posiadania kwalifikacji przez osoby zajmujące się eksploatacją urządzeń, instalacji i sieci.

        </p>
        
        <p>
        Świadectwa wydaje się w trzech grupach:<br /><br />
<strong>Grupa 1.</strong> – urządzenia, instalacje i sieci elektroenergetyczne wytwarzające, przetwarzające, przesyłające i zużywające energię elektryczną<br />
<strong>Grupa 2.</strong> – urządzenia wytwarzające, przetwarzające, przesyłające i zużywające ciepło oraz inne urządzenia energetyczne (np. pompy, wentylatory, ssawy, dmuchawy i sprężarki)<br />
<strong>Grupa 3.</strong> – urządzenia, instalacje i sieci gazowe wytwarzające, przetwarzające, przesyłające, magazynujące i zużywające paliwa gazowe.

        </p>
        
        <p>
        Rozróżnia się dwa rodzaje uprawnień kwalifikacyjnych:<br />
- eksploatacyjne (typu E), do których zalicza się stanowiska osób wykonujących prace w zakresie: obsługi, konserwacji, remontów, montażu i kontrolno-pomiarowe<br />
- dozorowe (typu D), do których zalicza się stanowiska osób kierujących czynnościami lub osób wykonujących prace w zakresie j. w. oraz stanowiska pracowników technicznych sprawujących nadzór nad eksploatacją urządzeń, instalacji i sieci. 

        </p>
		
		<p>
		Świadectwa kwalifikacyjne ważne są praktycznie przez okres pięciu lat od daty wydania.<br />

